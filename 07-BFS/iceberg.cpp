#include <stdio.h>
#include <memory.h>

#define SIZE 333
#define QSIZE 1111111

int map[SIZE][SIZE] = {0, };
int mapcpy[SIZE][SIZE] = {0, };
bool visited[SIZE][SIZE] = {false, };
int queue[QSIZE][2];
int row, col;
int i, j;
int rowdir[4] = {0, 0, 1, -1};
int coldir[4] = {1, -1, 0, 0};
int land, year = 0;
int front, rear;
bool is_no_iceberg = false;

void bfs(int row, int col);
void melt();

int main() {
	scanf("%d %d", &row, &col);
	for(i = 1; i <= row; i++) {
		for(j = 1; j <= col; j++) {
			scanf("%d", &map[i][j]);
		}
	}

	while(true) {
		land = 0;
		memset(visited, 0, sizeof(visited));
		for(i = 1; i <= row; i++) {
			for(j = 1; j <= col; j++) {
				if(map[i][j] > 0 && visited[i][j] == false) {
					land += 1;
					bfs(i, j);
				}
			}
		}
		if(land >= 2)
			break;
		else {
			if(land == 0) {
				year = 0;
				break;
			}
			year += 1;
			melt();
		}
	}
	printf("%d\n", year);
	return 0;
}

void bfs(int row, int col) {
	int frow, fcol, nrow, ncol;
	front = 1;
	rear = 1;
	queue[rear][0] = row;
	queue[rear++][1] = col;
	visited[row][col] = true;
	rear %= QSIZE;

	while(!(front == rear)) {
		frow = queue[front][0];
		fcol = queue[front++][1];
		front %= QSIZE;

		for(int i=0; i<4; i++) {
			nrow = frow + rowdir[i];
			ncol = fcol + coldir[i];

			if(map[nrow][ncol] > 0 && visited[nrow][ncol] == false) {
				queue[rear][0] = nrow;
				queue[rear++][1] = ncol;
				visited[nrow][ncol] = true;
				rear %= QSIZE;
			}
		}
	}
}

void melt() {
	memset(mapcpy, 0, sizeof(mapcpy));
	int cnt;
	for(int i=1; i<=row; i++) {
		for(int j=1; j<=col; j++) {
			cnt = 0;
			if(map[i][j] > 0) {
				for(int k=0; k<4; k++) {
					if(map[i + rowdir[k]][j + coldir[k]] <= 0)
						cnt++;
				}
				mapcpy[i][j] = map[i][j] - cnt;
				if(mapcpy[i][j] < 0)
					mapcpy[i][j] = 0;
			}
		}
	}
	memcpy(map, mapcpy, sizeof(map));
}

