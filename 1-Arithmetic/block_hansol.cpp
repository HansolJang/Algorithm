bool remove_once(int *arr)
{
	int pivot;
	bool is_end = true;
	for(int i=0; i<N; i++)
	{
		copy[i] = arr[i];
	}

	arr[0] = 0;
	arr[N-1] = 0;

	for(int i=1; i<N-1; i++)
	{
		if(arr[i] == 0)
			continue;
		pivot = MIN(copy[i-1], copy[i+1]);
		if(arr[i] == pivot)
			arr[i] = pivot - 1;
		else if(arr[i] > pivot)
			arr[i] = pivot;
		else
			arr[i] -= 1;

		if(arr[i] != 0)
			is_end = false;
	}

	// for(int i=0; i<N; i++)
	// {
	// 	printf("%d ", arr[i]);
	// }
	// printf("\n");

	return is_end;
}

